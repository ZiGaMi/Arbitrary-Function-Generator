#ifndef DRIVERS_UARTDRV_H_
#define DRIVERS_UARTDRV_H_

#include "stm32f0xx.h"
#include "stdbool.h"

#include "Drivers/ClockDrv.h"

////////////////////////////////////////////////////////////////////////////////
//
//		UART periphery
//
////////////////////////////////////////////////////////////////////////////////

#define UART					( USART1 )


////////////////////////////////////////////////////////////////////////////////
//
//		Port / Pins defines
//
////////////////////////////////////////////////////////////////////////////////

#define UART_port 				( GPIOA )

#define UART_TX_bp				( 2ul )
#define UART_TX_msk				( 0x01u << UART_TX_bp )
#define UART_TX_afnum			( 1ul )

#define UART_RX_bp				( 3ul )
#define UART_RX_msk				( 0x01u << UART_RX_bp )
#define UART_RX_afnum			( 1ul )



////////////////////////////////////////////////////////////////////////////////
//
//		UART Settings
//
////////////////////////////////////////////////////////////////////////////////

// Baud rate
#define UART_BAUDRATE			( 115200u )

// Reception buffer size
#define UART_RX_BUF_SIZE		( 100u )


////////////////////////////////////////////////////////////////////////////////
//
//		Macros
//
////////////////////////////////////////////////////////////////////////////////

#define UART_RX_BUSY_get()		( bool ) (( UART -> ISR & USART_ISR_BUSY ) == USART_ISR_BUSY )




////////////////////////////////////////////////////////////////////////////////
//
//		Function prototypes
//
////////////////////////////////////////////////////////////////////////////////

// Initialize uart
void 	UartInit				(void);

// Initialize uart DMA
void 	UartDmaInit				(uint8_t*);

// Send packet
void	UartSendString			(uint8_t*, uint32_t);


#endif /* DRIVERS_UARTDRV_H_ */
